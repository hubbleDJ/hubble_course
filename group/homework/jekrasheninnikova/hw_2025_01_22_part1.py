x: float = float(input(f'Введите первое число\n'))
y: float = float(input(f'Введите второе число\n'))

print(f'{x} + {y} = {x + y}')
print(f'{x} - {y} = {x - y}')
print(f'{x} * {y} = {x * y}')
print(f'{x} / {y} = {x / y}')
print(f'{x} % {y} = {x % y}')
print(f'{x} // {y} = {x // y}')
